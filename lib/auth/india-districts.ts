export const INDIA_DISTRICT_OPTIONS = RAW_INDIA_DISTRICTS.split("\n")
  .map((name) => name.trim())
  .filter(Boolean);

export function normalizeDistrictSearchValue(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, " ").trim();
}

export function getIndiaDistrictSuggestions(query: string, limit = 12) {
  const normalizedQuery = normalizeDistrictSearchValue(query);

  if (!normalizedQuery) {
    return INDIA_DISTRICT_OPTIONS.slice(0, limit);
  }

  return INDIA_DISTRICT_OPTIONS.filter((district) => {
    const normalizedDistrict = normalizeDistrictSearchValue(district);
    return normalizedDistrict.includes(normalizedQuery);
  }).slice(0, limit);
}
